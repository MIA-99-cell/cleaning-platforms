require('dotenv').config({ path: require('path').join(__dirname, '..', '.env') });
const pool = require('../src/config/database');

const SQL = `
CREATE TABLE IF NOT EXISTS platform_commissions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id INTEGER NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  source_type VARCHAR(30) NOT NULL CHECK (source_type IN ('booking_payment', 'product_order')),
  source_id INTEGER NOT NULL,
  gross_amount NUMERIC(12,2) NOT NULL,
  commission_rate NUMERIC(5,4) NOT NULL,
  commission_amount NUMERIC(12,2) NOT NULL,
  period_month CHAR(7) NOT NULL,
  created_at TIMESTAMP DEFAULT NOW(),
  UNIQUE (source_type, source_id)
);

CREATE INDEX IF NOT EXISTS idx_platform_commissions_tenant_id ON platform_commissions(tenant_id);
CREATE INDEX IF NOT EXISTS idx_platform_commissions_period_month ON platform_commissions(period_month);
`;

(async () => {
  await pool.query(SQL);
  const [rows] = await pool.query('SELECT COUNT(*) AS n FROM platform_commissions');
  console.log('platform_commissions table ready. rows:', rows[0]?.n ?? 0);
  process.exit(0);
})().catch((e) => {
  console.error(e);
  process.exit(1);
});
