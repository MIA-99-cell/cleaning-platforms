require('dotenv').config({ path: require('path').join(__dirname, '..', '.env') });
const pool = require('../src/config/database');

const SQL = `
CREATE TABLE IF NOT EXISTS products (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id INTEGER NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  name VARCHAR(255) NOT NULL,
  description TEXT,
  price NUMERIC(10,2) NOT NULL,
  image_url VARCHAR(500),
  stock_quantity INTEGER DEFAULT 0,
  is_active BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMP DEFAULT NOW(),
  updated_at TIMESTAMP DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_products_tenant_id ON products(tenant_id);
CREATE INDEX IF NOT EXISTS idx_products_is_active ON products(is_active);

CREATE TABLE IF NOT EXISTS product_orders (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id INTEGER NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  product_id INTEGER NOT NULL REFERENCES products(id),
  customer_id INTEGER NOT NULL REFERENCES customers(id),
  quantity INTEGER NOT NULL DEFAULT 1,
  unit_price NUMERIC(10,2) NOT NULL,
  total_amount NUMERIC(10,2) NOT NULL,
  payment_method VARCHAR(30) NOT NULL CHECK (payment_method IN ('cash_on_delivery', 'mobile_money')),
  status VARCHAR(30) DEFAULT 'placed' CHECK (status IN ('placed', 'payment_pending', 'paid', 'delivered', 'cancelled')),
  delivery_address TEXT NOT NULL,
  delivery_phone VARCHAR(50),
  transaction_ref VARCHAR(255),
  notes TEXT,
  confirmed_at TIMESTAMP,
  confirmed_by INTEGER,
  created_at TIMESTAMP DEFAULT NOW(),
  updated_at TIMESTAMP DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_product_orders_tenant_id ON product_orders(tenant_id);
CREATE INDEX IF NOT EXISTS idx_product_orders_customer_id ON product_orders(customer_id);
CREATE INDEX IF NOT EXISTS idx_product_orders_product_id ON product_orders(product_id);
`;

(async () => {
  await pool.query(SQL);
  const [products] = await pool.query('SELECT COUNT(*) AS n FROM products');
  const [orders] = await pool.query('SELECT COUNT(*) AS n FROM product_orders');
  console.log('Marketplace tables ready. products:', products[0]?.n, 'orders:', orders[0]?.n);
  process.exit(0);
})().catch((e) => {
  console.error(e);
  process.exit(1);
});
