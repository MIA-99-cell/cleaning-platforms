require('dotenv').config({ path: require('path').join(__dirname, '..', '.env') });
const pool = require('../src/config/database');

const CREATE_TABLE = `
CREATE TABLE IF NOT EXISTS cleaner_assignments (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id INTEGER NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  booking_id INTEGER NOT NULL REFERENCES bookings(id) ON DELETE CASCADE,
  cleaner_id INTEGER NOT NULL REFERENCES cleaners(id),
  status VARCHAR(20) DEFAULT 'assigned' CHECK (status IN ('assigned','accepted','rejected','in_progress','completed')),
  notes TEXT,
  completion_photo_url VARCHAR(500),
  started_at TIMESTAMP,
  completed_at TIMESTAMP,
  created_at TIMESTAMP DEFAULT NOW(),
  updated_at TIMESTAMP DEFAULT NOW(),
  UNIQUE (booking_id, cleaner_id)
)`;

const CREATE_INDEXES = [
  'CREATE INDEX IF NOT EXISTS idx_cleaner_assignments_tenant_id ON cleaner_assignments(tenant_id)',
  'CREATE INDEX IF NOT EXISTS idx_cleaner_assignments_cleaner_id ON cleaner_assignments(cleaner_id)',
  'CREATE INDEX IF NOT EXISTS idx_cleaner_assignments_booking_id ON cleaner_assignments(booking_id)',
];

(async () => {
  const [existing] = await pool.query(`
    SELECT table_name FROM information_schema.tables
    WHERE table_schema = 'public' AND table_name = 'cleaner_assignments'
  `);

  if (existing.length) {
    console.log('cleaner_assignments table already exists');
  } else {
    console.log('Creating cleaner_assignments table...');
    await pool.query(CREATE_TABLE);
    for (const sql of CREATE_INDEXES) {
      await pool.query(sql);
    }
    console.log('cleaner_assignments table created');
  }

  const [count] = await pool.query('SELECT COUNT(*) AS n FROM cleaner_assignments');
  console.log('cleaner_assignments rows:', count[0]?.n ?? count[0]);

  process.exit(0);
})().catch((e) => {
  console.error(e);
  process.exit(1);
});
