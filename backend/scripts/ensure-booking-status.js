require('dotenv').config({ path: require('path').join(__dirname, '..', '.env') });
const pool = require('../src/config/database');
const fs = require('fs');
const path = require('path');

(async () => {
  const [tables] = await pool.query(`
    SELECT table_name FROM information_schema.tables
    WHERE table_schema = 'public' AND table_name LIKE '%booking%'
    ORDER BY table_name
  `);
  console.log('booking tables:', tables.map((t) => t.table_name));

  const hasStatus = tables.some((t) => t.table_name === 'booking_status');
  if (!hasStatus) {
    console.log('Creating booking_status table and seeding...');
    const sql = fs.readFileSync(path.join(__dirname, '..', 'database', 'schema.supabase.sql'), 'utf8');
    const statusBlock = sql.match(/create table if not exists booking_status[\s\S]*?;\s*create table if not exists bookings/);
    if (statusBlock) {
      await pool.query(statusBlock[0].replace(/\s*create table if not exists bookings$/, ''));
    } else {
      await pool.query(`
        create table if not exists booking_status (
          id integer generated by default as identity primary key,
          name varchar(50) not null unique,
          description varchar(255),
          created_at timestamp default now()
        )
      `);
    }
    await pool.query(`
      insert into booking_status (name, description) values
        ('pending', 'Awaiting confirmation'),
        ('confirmed', 'Confirmed by tenant'),
        ('in_progress', 'Cleaner is working'),
        ('completed', 'Job completed'),
        ('cancelled', 'Booking cancelled')
      on conflict (name) do nothing
    `);
    console.log('booking_status ready');
  }
  process.exit(0);
})().catch((e) => {
  console.error(e);
  process.exit(1);
});
